							<h2>Heading Dua Lorem Ipsum</h2>
							<p>Masih dalam rangka memperingati hari kemerdekaan Indonesia, Kamis (27/8/2020) Fakultas Ilmu Komputer Universitas Indonesia (Fasilkom UI)&nbsp;bekerja sama dengan Institute of Electrical &amp; Electronics Engineers (IEEE) Indonesia Section&nbsp;menggelar seminar daring dengan tema “Mencitakan Masyarakat Well-educated untuk Indonesia Maju”.</p>
							<p>Pembicara dalam seminar ini adalah Prof. Dr. Adiwijaya, S.Si, M.Si (Rektor Telkom University). Dalam pemaparannya, ia mengatakan bahwa Indonesia sebenarnya adalah negara kaya, tapi punya kebiasaan mental seperti negara yang berkekurangan.</p>
							<h2>Heading Dua Juga</h2>
							<h3>Heading Tiga</h3>
							<p><div class="img"><img src="img/img1.jpg"><span>Kekayaan Alam Indonesia</span></div></p>
							<p>“Produksi nikel kita nomor 1 di dunia, timah nomor 2, bauksit nomor 6, karet nomor 2, kakao nomor 3, kopi nomor 4, minyak bumi nomor 23 di dunia, tapi apakah semua kekayaan tersebut dapat membuat kita menjadi lebih maju? Itu pertanyaannya,” katanya.</p>
							<h4>Heading Empat</h4>
							<h5>Heading Lima</h5>
							<p>Menurutnya, bukan sumber daya alam yang membuat suatu negara menjadi maju, namun pendidikan. Pendidikan yang mempunyai relevansi dengan zaman.</p>
							<h5>Heading Lima</h5>
							<p>Pendidikan di era revolusi industri 4.0 mempunyai delapan ciri khas, yaitu dikuasainya kemampuan teknologi, adanya kemampuan interpersonal yang baik, mampu berpikir kreatif, mampu berkolaborasi dan memecahkan masalah, berwawasan luas dan inklusif, berwawasan global , serta punya daya pembelajar seumur hidup.</p>
							<p>Salah satu cara untuk menciptakan pendidikan dengan ciri khas seperti itu adalah dengan menciptakan budaya/kultur. Kultur yang baik hanya dapat diciptakan melalui kebiasaan yang baik, salah satunya adalah kebiasaan “kesungguhan” (<em>passion</em>).</p>
							<p><div class="img"><img src="img/img3.jpg"><span>Pendidikan adalah kunci</span></div></p>
							<p>“Banyak anak muda yang salah mengartikan <em>passion</em>, seolah-olah <em>passion</em>&nbsp;itu ditemukan. Makanya akhirnya <em>gak</em>&nbsp;pernah ketemu. Padahal <em>passion</em>&nbsp;itu adalah mengerjakan apa yang kita geluti dengan rasa syukur dan kesungguhan. <em>Itu passion</em>,” ujarnya menjelaskan.</p>
							<p>Dengan membangun kebiasaan-kebiasaan baik tersebut, maka seorang individu dapat membangun sebuah pola pikir yang <em>well-educated</em>. Ciri khas dari pola pikir ini adalah punya karakteristik adaptif dan peka dengan perubahan, mau terus melakukan koreksi diri, serta dapat menjadi manusia yang baik yang patuh terhadap Tuhan dan negara tempat ia tinggal.</p>
							<p>“Pada akhirnya, <em>habit</em> atau kebiasaaan yang baik akan selalu menghasilkan kultur yang baik, dan kultur yang baik akan selalu menghasilkan kemajuan, baik di tingkat individu maupun negara,” katanya di akhir pemaparan.</p>
							<p>Setelah pemaparan berlangsung, agenda dilanjutkan dengan sesi diskusi panel yang diisi oleh Prof. Drs. Heru Suhartanto, M.Sc., Ph.D (Guru Besar Fasilkom UI), Mayor Jenderal (Purn.) H.E. Dr. Arief Rachman, M.D. (Duta Besar RI untuk Afghanistan), serta Fariz Darari, Ph.D (Co-director Tokopedia-UI AI Center of Excellence).</p>
